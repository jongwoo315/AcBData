package doit;

public class scoreDTO {
	private int num;
	private String name;
	private String kor;
	private String eng;
	private String math;
		
	public int getNum() { return num; }
	public void setNum(int a) { this.num = a; }
	
	public String getName() { return name; }
	public void setName(String name) { this.name = name; }
	
	public String getKor() { return kor; }
	public void setKor(String bb) { this.kor = bb; }
	
	public String getEng() { return eng; }
	public void setEng(String c) { this.eng = c; }
	
	public String getMath() { return math; }
	public void setMath(String d) { this.math = d; }
}